# Uses run-length compression to optimise output arrays from LEDMatrixStudio

def rle_compress(input_array):
    if not input_array:
        return []

    compressed = []
    current_element = input_array[0]
    count = 1

    for element in input_array[1:]:
        if element == current_element:
            count += 1
        else:
            compressed.append((current_element, count))
            current_element = element
            count = 1

    compressed.append((current_element, count))
    return compressed

def format_run_length(encoded, pairs_per_line=1):
    formatted_output = "{\n"
    line = "    "
    pair_count = 0

    for count, value in encoded:
        line += f"{count}, {value}, "
        pair_count += 1
        if pair_count >= pairs_per_line:
            formatted_output += line.rstrip() + "\n"
            line = "    "
            pair_count = 0

    if line.strip():
        formatted_output += line.rstrip() + "\n"

    formatted_output += "};"
    return formatted_output

